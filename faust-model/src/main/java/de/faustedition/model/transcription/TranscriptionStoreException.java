package de.faustedition.model.transcription;

public class TranscriptionStoreException extends Exception {

	public TranscriptionStoreException() {
		super();
	}

	public TranscriptionStoreException(String message, Throwable cause) {
		super(message, cause);
	}

	public TranscriptionStoreException(String message) {
		super(message);
	}

	public TranscriptionStoreException(Throwable cause) {
		super(cause);
	}

}
