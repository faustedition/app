package de.faustedition.util;

public class XMLException extends RuntimeException
{

	public XMLException()
	{
		super();
	}

	public XMLException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public XMLException(String message)
	{
		super(message);
	}

	public XMLException(Throwable cause)
	{
		super(cause);
	}

}
