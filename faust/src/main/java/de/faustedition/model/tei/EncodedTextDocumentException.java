package de.faustedition.model.tei;

import de.faustedition.util.XMLException;

public class EncodedTextDocumentException extends XMLException {

	public EncodedTextDocumentException() {
		super();
	}

	public EncodedTextDocumentException(String message, Throwable cause) {
		super(message, cause);
	}

	public EncodedTextDocumentException(String message) {
		super(message);
	}

	public EncodedTextDocumentException(Throwable cause) {
		super(cause);
	}

}
